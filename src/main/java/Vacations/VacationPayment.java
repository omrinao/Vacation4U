package Vacations;

public class VacationPayment {

    public String _vID;
    public String _seller;
    public String _date;
    public String _price;

    public VacationPayment(String _vID, String _seller, String _date, String _price) {
        this._vID = _vID;
        this._seller = _seller;
        this._date = _date;
        this._price = _price;
    }
}
